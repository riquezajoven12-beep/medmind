// PDF Export (using jsPDF)
// ============================================
export async function exportToPDF(nodes: MindMapNode[], title: string): Promise<void> {
  const pdf = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
  const roots = nodes.filter(n => !n.parentId);
  const pageW = 210, pageH = 297;
  const margin = 25;
  const contentW = pageW - margin * 2;
  let y = margin;

  const colors = {
    brand: [6, 214, 160] as [number, number, number],
    purple: [124, 58, 237] as [number, number, number],
    pink: [247, 37, 133] as [number, number, number],
    amber: [251, 191, 36] as [number, number, number],
    blue: [0, 180, 216] as [number, number, number],
    text: [30, 41, 59] as [number, number, number],
    textMuted: [100, 116, 139] as [number, number, number],
    light: [148, 163, 184] as [number, number, number],
  };
  const accentArr = [colors.brand, colors.purple, colors.pink, colors.amber, colors.blue];

  function checkPageBreak(needed: number) {
    if (y + needed > pageH - margin) {
      pdf.addPage();
      y = margin;
      // Header
      pdf.setFontSize(8);
      pdf.setTextColor(...colors.light);
      pdf.text(`${title}  Â·  MedMind AI`, pageW - margin, 15, { align: 'right' });
      y = 25;
    }
  }

  function addLine(color: [number, number, number]) {
    pdf.setDrawColor(...color);
    pdf.setLineWidth(0.5);
    pdf.line(margin, y, margin + contentW, y);
    y += 5;
  }

  // ---- Title Page ----
  // Accent bar
  pdf.setFillColor(...colors.brand);
  pdf.rect(0, 0, 3, pageH, 'F');

  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(36);
  pdf.setTextColor(...colors.text);
  pdf.text(title, margin + 5, 80);

  pdf.setFont('helvetica', 'normal');
  pdf.setFontSize(12);
  pdf.setTextColor(colors.textMuted[0], colors.textMuted[1], colors.textMuted[2]);
  pdf.text(`${nodes.length} topics Â· Generated by MedMind AI`, margin + 5, 95);

  pdf.setFontSize(10);
  pdf.setTextColor(...colors.light);
  pdf.text(new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' }), margin + 5, 105);

  // ---- TOC ----
  pdf.addPage();
  y = margin;

  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(22);
  pdf.setTextColor(...colors.text);
  pdf.text('Table of Contents', margin, y);
  y += 15;

  roots.forEach((root, i) => {
    checkPageBreak(20);
    const accent = accentArr[i % accentArr.length];
    
    pdf.setFillColor(...accent);
    pdf.rect(margin, y - 3, 3, 3, 'F');
    
    pdf.setFont('helvetica', 'bold');
    pdf.setFontSize(12);
    pdf.setTextColor(...colors.text);
    pdf.text(root.data.title, margin + 8, y);
    y += 7;

    getChildren(nodes, root.id).forEach(child => {
      checkPageBreak(7);
      pdf.setFont('helvetica', 'normal');
      pdf.setFontSize(10);
      pdf.setTextColor(colors.textMuted[0], colors.textMuted[1], colors.textMuted[2]);
      pdf.text(`â–¸  ${child.data.title}`, margin + 15, y);
      y += 5;
    });
    y += 3;
  });

  // ---- Content ----
  roots.forEach((root, rootIdx) => {
    pdf.addPage();
    y = margin;
    const accent = accentArr[rootIdx % accentArr.length];

    // Accent bar top
    pdf.setFillColor(...accent);
    pdf.rect(0, 0, pageW, 2, 'F');

    // H1
    pdf.setFont('helvetica', 'bold');
    pdf.setFontSize(24);
    pdf.setTextColor(...accent);
    pdf.text(root.data.title, margin, y + 8);
    y += 15;

    addLine(accent);

    if (root.data.content) {
      checkPageBreak(15);
      pdf.setFont('helvetica', 'normal');
      pdf.setFontSize(10);
      pdf.setTextColor(colors.textMuted[0], colors.textMuted[1], colors.textMuted[2]);
      const lines = pdf.splitTextToSize(root.data.content, contentW);
      pdf.text(lines, margin, y);
      y += lines.length * 5 + 5;
    }

    // Build content recursively
    renderPDFChildren(pdf, nodes, root.id, 2, margin, contentW, pageH, colors, accent,
      () => y, (newY: number) => { y = newY; },
      () => checkPageBreak(15)
    );
  });

  // Footer on each page
  const totalPages = pdf.getNumberOfPages();
  for (let i = 1; i <= totalPages; i++) {
    pdf.setPage(i);
    pdf.setFontSize(8);
    pdf.setTextColor(...colors.light);
    pdf.text(`Page ${i} of ${totalPages}`, pageW / 2, pageH - 10, { align: 'center' });
  }

  const filename = `${title.replace(/[^a-zA-Z0-9\s]/g, '').replace(/\s+/g, '_')}.pdf`;
  pdf.save(filename);
}

function renderPDFChildren(
  pdf: jsPDF, nodes: MindMapNode[], parentId: string, level: number,
  margin: number, contentW: number, pageH: number,
  colors: any, accent: [number, number, number],
  getY: () => number, setY: (y: number) => void,
  checkBreak: () => void
) {
  const children = getChildren(nodes, parentId);
  const indent = Math.min((level - 1) * 8, 24);
  const fontSizes = [24, 16, 13, 11, 10];
  const fontSize = fontSizes[Math.min(level - 1, 4)];

  children.forEach(child => {
    let y = getY();
    checkBreak();
    y = getY();

    // Heading
    pdf.setFont('helvetica', level <= 2 ? 'bold' : 'normal');
    pdf.setFontSize(fontSize);
    const headingColor = level <= 2 ? accent : (colors.text as [number, number, number]);
    pdf.setTextColor(headingColor[0], headingColor[1], headingColor[2]);
    
    const titlePrefix = level > 2 ? 'â–¸ ' : '';
    pdf.text(`${titlePrefix}${child.data.title}`, margin + indent, y);
    y += fontSize * 0.5 + 3;

    // Content
    if (child.data.content) {
      pdf.setFont('helvetica', 'normal');
      pdf.setFontSize(9);
      pdf.setTextColor(colors.textMuted[0], colors.textMuted[1], colors.textMuted[2]);
      const lines = pdf.splitTextToSize(child.data.content, contentW - indent);
      pdf.text(lines, margin + indent + (level > 2 ? 5 : 0), y);
      y += lines.length * 4 + 3;
    }

    y += 2;
    setY(y);

    renderPDFChildren(pdf, nodes, child.id, level + 1, margin, contentW, pageH, colors, accent, getY, setY, checkBreak);
  });
}

// ============================================
// Script Loader
// ============================================
function loadScript<T>(url: string, globalName: string): Promise<T> {
  return new Promise((resolve, reject) => {
    // Check if already loaded
    if ((window as any)[globalName]) {
      resolve((window as any)[globalName]);
      return;
    }

    const script = document.createElement('script');
    script.src = url;
    script.onload = () => {
      if ((window as any)[globalName]) {
        resolve((window as any)[globalName]);
      } else {
        // Some UMD libs attach differently
        setTimeout(() => {
          resolve((window as any)[globalName]);
        }, 100);
      }
    };
    script.onerror = () => reject(new Error(`Failed to load ${url}`));
    document.head.appendChild(script);
  });
}

// ============================================
// Master Export Function
// ============================================
export async function exportMindMap(
  format: 'pptx' | 'docx' | 'pdf' | 'png' | 'json' | 'markdown',
  nodes: MindMapNode[],
  title: string,
  canvasElement?: HTMLElement
): Promise<void> {
  switch (format) {
    case 'pptx':
      await exportToPPTX(nodes, title);
      break;
    case 'docx':
      await exportToDOCX(nodes, title);
      break;
    case 'pdf':
      await exportToPDF(nodes, title);
      break;
    case 'png':
      if (canvasElement) {
        const { default: html2canvas } = await import('html-to-image' as any).catch(() => {
          // Fallback: use dom-to-image pattern
          return { default: null };
        });
        // Simple canvas screenshot approach
        const dataUrl = await toPng(canvasElement);
        const link = document.createElement('a');
        link.download = `${title.replace(/[^a-zA-Z0-9]/g, '_')}.png`;
        link.href = dataUrl;
        link.click();
      }
      break;
    case 'json':
      const jsonBlob = new Blob([JSON.stringify({ title, nodes, version: 2, exportedAt: new Date().toISOString() }, null, 2)], { type: 'application/json' });
      downloadBlob(jsonBlob, `${title.replace(/[^a-zA-Z0-9]/g, '_')}.json`);
      break;
    case 'markdown':
      const md = generateMarkdown(nodes, title);
      downloadBlob(new Blob([md], { type: 'text/markdown' }), `${title.replace(/[^a-zA-Z0-9]/g, '_')}.md`);
      break;
  }
}

async function toPng(element: HTMLElement): Promise<string> {
  const { toPng: toImage } = await import('html-to-image');
  return toImage(element, { quality: 0.95, backgroundColor: '#0a0e17', pixelRatio: 2 });
}

function generateMarkdown(nodes: MindMapNode[], title: string): string {
  let md = `# ${title}\n\n`;
  md += `> Generated by MedMind AI Â· ${new Date().toLocaleDateString()}\n\n---\n\n`;

  const roots = nodes.filter(n => !n.parentId);
  roots.forEach(root => {
    md += `## ${root.data.title}\n\n`;
    if (root.data.content) md += `${root.data.content}\n\n`;
    md += buildMdChildren(nodes, root.id, 3);
  });

  return md;
}

function buildMdChildren(nodes: MindMapNode[], parentId: string, level: number): string {
  const children = getChildren(nodes, parentId);
  let md = '';
  children.forEach(child => {
    const hashes = '#'.repeat(Math.min(level, 6));
    const aiTag = child.data.aiGenerated ? ' âœ¨' : '';
    md += `${hashes} ${child.data.title}${aiTag}\n\n`;
    if (child.data.content) md += `${child.data.content}\n\n`;
    if (child.data.tags?.length) md += `*Tags: ${child.data.tags.join(', ')}*\n\n`;
    md += buildMdChildren(nodes, child.id, level + 1);
  });
  return md;
}

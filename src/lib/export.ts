// DOCX Export (using docx.js loaded from CDN)
// ============================================
export async function exportToDOCX(nodes: MindMapNode[], title: string): Promise<void> {
  const docx = await loadScript<any>(
    'https://cdn.jsdelivr.net/npm/docx@8.5.0/build/index.umd.js',
    'docx'
  );

  const { Document, Packer, Paragraph, TextRun, HeadingLevel, AlignmentType, 
          BorderStyle, PageBreak, TableOfContents, Header, Footer, PageNumber } = docx;

  const roots = nodes.filter(n => !n.parentId);
  const sections: any[] = [];

  // Title page
  sections.push(
    new Paragraph({
      spacing: { before: 4000 },
      alignment: AlignmentType.CENTER,
      children: [
        new TextRun({ text: title, bold: true, size: 56, font: 'Georgia', color: '06d6a0' }),
      ],
    }),
    new Paragraph({
      alignment: AlignmentType.CENTER,
      spacing: { before: 400 },
      children: [
        new TextRun({ text: `${nodes.length} topics Â· Generated by MedMind AI`, size: 22, color: '94a3b8', font: 'Arial' }),
      ],
    }),
    new Paragraph({
      alignment: AlignmentType.CENTER,
      spacing: { before: 200 },
      children: [
        new TextRun({
          text: new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' }),
          size: 20, color: '64748b', font: 'Arial',
        }),
      ],
    }),
    new Paragraph({ children: [new PageBreak()] }),
  );

  // Table of contents header
  sections.push(
    new Paragraph({
      heading: HeadingLevel.HEADING_1,
      children: [new TextRun({ text: 'Table of Contents', bold: true, size: 32, font: 'Georgia' })],
    }),
    new Paragraph({ spacing: { after: 200 }, children: [] }),
  );

  // TOC entries
  roots.forEach(root => {
    sections.push(
      new Paragraph({
        spacing: { before: 100 },
        children: [new TextRun({ text: `â–   ${root.data.title}`, size: 22, font: 'Arial' })],
      })
    );
    getChildren(nodes, root.id).forEach(child => {
      sections.push(
        new Paragraph({
          indent: { left: 720 },
          children: [new TextRun({ text: `â–¸  ${child.data.title}`, size: 20, color: '64748b', font: 'Arial' })],
        })
      );
    });
  });

  sections.push(new Paragraph({ children: [new PageBreak()] }));

  // Content
  roots.forEach((root, idx) => {
    const accentColors = ['06d6a0', '7c3aed', 'f72585', 'fbbf24', '00b4d8'];
    const accent = accentColors[idx % accentColors.length];

    // H1
    sections.push(
      new Paragraph({
        heading: HeadingLevel.HEADING_1,
        spacing: { before: 400, after: 200 },
        border: { bottom: { style: BorderStyle.SINGLE, size: 6, color: accent } },
        children: [new TextRun({ text: root.data.title, bold: true, size: 32, font: 'Georgia', color: accent })],
      })
    );

    if (root.data.content) {
      sections.push(
        new Paragraph({
          spacing: { after: 200 },
          children: [new TextRun({ text: root.data.content, size: 22, font: 'Arial', color: '475569' })],
        })
      );
    }

    // Children
    buildDocxSections(nodes, root.id, 2, sections, docx, accent);
  });

  const doc = new Document({
    creator: 'MedMind AI',
    title: title,
    description: `Medical mind map: ${title}`,
    styles: {
      default: {
        document: {
          run: { font: 'Arial', size: 22, color: '1e293b' },
          paragraph: { spacing: { line: 340 } },
        },
      },
    },
    sections: [{
      properties: {
        page: {
          margin: { top: 1440, right: 1440, bottom: 1440, left: 1440 },
        },
      },
      headers: {
        default: new Header({
          children: [new Paragraph({
            alignment: AlignmentType.RIGHT,
            children: [new TextRun({ text: `${title}  Â·  MedMind AI`, size: 16, color: '94a3b8', font: 'Arial' })],
          })],
        }),
      },
      footers: {
        default: new Footer({
          children: [new Paragraph({
            alignment: AlignmentType.CENTER,
            children: [
              new TextRun({ text: 'Page ', size: 16, color: '94a3b8' }),
              new TextRun({ children: [PageNumber.CURRENT], size: 16, color: '94a3b8' }),
              new TextRun({ text: ' of ', size: 16, color: '94a3b8' }),
              new TextRun({ children: [PageNumber.TOTAL_PAGES], size: 16, color: '94a3b8' }),
            ],
          })],
        }),
      },
      children: sections,
    }],
  });

  const blob = await Packer.toBlob(doc);
  const filename = `${title.replace(/[^a-zA-Z0-9\s]/g, '').replace(/\s+/g, '_')}.docx`;
  downloadBlob(blob, filename);
}

function buildDocxSections(nodes: MindMapNode[], parentId: string, level: number, sections: any[], docx: any, accent: string) {
  const { Paragraph, TextRun, HeadingLevel, BorderStyle } = docx;
  const children = getChildren(nodes, parentId);

  const headingLevels = [
    HeadingLevel.HEADING_1, HeadingLevel.HEADING_2, HeadingLevel.HEADING_3,
    HeadingLevel.HEADING_4, HeadingLevel.HEADING_5, HeadingLevel.HEADING_6,
  ];

  children.forEach(child => {
    // Heading
    sections.push(
      new Paragraph({
        heading: headingLevels[Math.min(level - 1, 5)],
        spacing: { before: 300, after: 100 },
        children: [
          new TextRun({
            text: child.data.title,
            bold: true,
            size: Math.max(20, 30 - level * 3),
            font: level <= 2 ? 'Georgia' : 'Arial',
            color: level <= 2 ? accent : '334155',
          }),
          child.data.aiGenerated ? new TextRun({ text: '  âœ¨', size: 16 }) : new TextRun(''),
        ],
      })
    );

    // Content
    if (child.data.content) {
      sections.push(
        new Paragraph({
          spacing: { after: 200 },
          children: [new TextRun({
            text: child.data.content,
            size: 21,
            font: 'Arial',
            color: '475569',
          })],
        })
      );
    }

    // Tags
    if (child.data.tags?.length > 0) {
      sections.push(
        new Paragraph({
          spacing: { after: 100 },
          children: [new TextRun({
            text: `Tags: ${child.data.tags.join(', ')}`,
            size: 16,
            font: 'Arial',
            color: '94a3b8',
            italics: true,
          })],
        })
      );
    }

    // Recurse
    buildDocxSections(nodes, child.id, level + 1, sections, docx, accent);
  });
}


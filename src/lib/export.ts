// PPTX Export (using PptxGenJS loaded from CDN)
// ============================================
export async function exportToPPTX(nodes: MindMapNode[], title: string): Promise<void> {
  // Dynamically load PptxGenJS
  const PptxGenJS = await loadScript<any>(
    'https://cdn.jsdelivr.net/npm/pptxgenjs@3.12.0/dist/pptxgen.bundle.js',
    'PptxGenJS'
  );

  const pptx = new PptxGenJS();
  pptx.author = 'MedMind AI';
  pptx.title = title;
  pptx.subject = 'Medical Mind Map';
  pptx.layout = 'LAYOUT_WIDE';

  // Color palette
  const colors = {
    bg: '0a0e17',
    bgLight: '1a2236',
    surface: '253349',
    text: 'e8edf5',
    textMuted: '94a3b8',
    brand: '06d6a0',
    purple: '7c3aed',
    pink: 'f72585',
    amber: 'fbbf24',
    blue: '00b4d8',
  };

  const accents = [colors.brand, colors.purple, colors.pink, colors.amber, colors.blue];
  const roots = nodes.filter(n => !n.parentId);

  // ---- Title Slide ----
  const titleSlide = pptx.addSlide();
  titleSlide.background = { color: colors.bg };
  
  // Decorative accent bar
  titleSlide.addShape(pptx.shapes.RECTANGLE, {
    x: 0, y: 0, w: 0.08, h: '100%', fill: { color: colors.brand },
  });

  titleSlide.addText(title, {
    x: 1, y: 1.5, w: 10, h: 1.5,
    fontSize: 44, fontFace: 'Georgia', color: colors.text, bold: true,
  });

  titleSlide.addText(`${nodes.length} topics Â· Generated by MedMind AI`, {
    x: 1, y: 3.2, w: 10, h: 0.5,
    fontSize: 14, fontFace: 'Arial', color: colors.textMuted,
  });

  titleSlide.addText(new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' }), {
    x: 1, y: 3.8, w: 10, h: 0.4,
    fontSize: 11, fontFace: 'Arial', color: '64748b',
  });

  // ---- Overview Slide ----
  if (roots.length > 0) {
    const overviewSlide = pptx.addSlide();
    overviewSlide.background = { color: colors.bg };
    overviewSlide.addShape(pptx.shapes.RECTANGLE, { x: 0, y: 0, w: '100%', h: 0.04, fill: { color: colors.brand } });

    overviewSlide.addText('Overview', {
      x: 0.8, y: 0.4, w: 10, h: 0.7,
      fontSize: 28, fontFace: 'Georgia', color: colors.text, bold: true,
    });

    roots.forEach((root, i) => {
      const accent = accents[i % accents.length];
      const y = 1.5 + i * 0.65;
      
      overviewSlide.addShape(pptx.shapes.RECTANGLE, {
        x: 0.8, y: y + 0.08, w: 0.25, h: 0.25, fill: { color: accent },
        rectRadius: 0.04,
      });

      overviewSlide.addText(root.data.title, {
        x: 1.3, y, w: 9, h: 0.45,
        fontSize: 16, fontFace: 'Arial', color: colors.text,
      });
    });
  }

  // ---- Content Slides ----
  roots.forEach((root, rootIdx) => {
    const children = getChildren(nodes, root.id);
    const accent = accents[rootIdx % accents.length];

    // Root topic slide with children as bullets
    const rootSlide = pptx.addSlide();
    rootSlide.background = { color: colors.bg };
    rootSlide.addShape(pptx.shapes.RECTANGLE, { x: 0, y: 0, w: '100%', h: 0.04, fill: { color: accent } });

    rootSlide.addText(root.data.title, {
      x: 0.8, y: 0.4, w: 10, h: 0.8,
      fontSize: 28, fontFace: 'Georgia', color: colors.text, bold: true,
    });

    if (root.data.content) {
      rootSlide.addText(root.data.content, {
        x: 0.8, y: 1.2, w: 10, h: 0.6,
        fontSize: 13, fontFace: 'Arial', color: colors.textMuted, italic: true,
      });
    }

    // List children
    children.forEach((child, ci) => {
      const y = (root.data.content ? 2.0 : 1.5) + ci * 0.55;
      
      rootSlide.addText('â–¸', {
        x: 0.8, y, w: 0.3, h: 0.4,
        fontSize: 14, fontFace: 'Arial', color: accent,
      });

      rootSlide.addText(child.data.title, {
        x: 1.2, y, w: 9.5, h: 0.4,
        fontSize: 15, fontFace: 'Arial', color: colors.text,
      });
    });

    // Detail slides for each child
    children.forEach((child, ci) => {
      const grandchildren = getChildren(nodes, child.id);
      if (grandchildren.length === 0 && !child.data.content) return;

      const childSlide = pptx.addSlide();
      childSlide.background = { color: colors.bg };
      childSlide.addShape(pptx.shapes.RECTANGLE, { x: 0, y: 0, w: '100%', h: 0.04, fill: { color: accent } });

      // Breadcrumb
      childSlide.addText(`${root.data.title}  â€º`, {
        x: 0.8, y: 0.3, w: 5, h: 0.3,
        fontSize: 10, fontFace: 'Arial', color: '64748b',
      });

      childSlide.addText(child.data.title, {
        x: 0.8, y: 0.6, w: 10, h: 0.7,
        fontSize: 24, fontFace: 'Georgia', color: colors.text, bold: true,
      });

      if (child.data.content) {
        childSlide.addText(child.data.content, {
          x: 0.8, y: 1.4, w: 10, h: 0.8,
          fontSize: 13, fontFace: 'Arial', color: colors.textMuted,
          lineSpacingMultiple: 1.4,
        });
      }

      // Grandchildren
      const startY = child.data.content ? 2.4 : 1.6;
      grandchildren.forEach((gc, gi) => {
        const y = startY + gi * 0.7;
        
        childSlide.addText(`â–¸  ${gc.data.title}`, {
          x: 0.8, y, w: 10, h: 0.3,
          fontSize: 14, fontFace: 'Arial', color: colors.text, bold: true,
        });

        if (gc.data.content) {
          childSlide.addText(gc.data.content.substring(0, 150), {
            x: 1.2, y: y + 0.3, w: 9.5, h: 0.3,
            fontSize: 11, fontFace: 'Arial', color: colors.textMuted,
          });
        }
      });
    });
  });

  // Save
  const filename = `${title.replace(/[^a-zA-Z0-9\s]/g, '').replace(/\s+/g, '_')}.pptx`;
  await pptx.writeFile({ fileName: filename });
}

